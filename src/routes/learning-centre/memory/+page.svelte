<div class="mx-20">
	<p>
		In contrast to other direct-access data storage media (such as hard discs, CD-RWs, DVD-RWs, and
		the older magnetic tapes and drum memory), random-access memory (RAM) allows data items to be
		read or written in almost the same amount of time regardless of the physical location of data
		inside the memory. Random-access memory is typically used to store working data and machine
		code.
	</p>
	<div class="divider" />
	<p>
		In order to connect the data lines to the designated storage for reading or writing the entry,
		RAM contains multiplexing and demultiplexing hardware. The same address is typically used to
		access many bits of storage, thus RAM devices frequently contain multiple data lines and are
		referred to as "8-bit," "16-bit," etc. devices.
	</p>
	<div class="divider" />
	<p>
		Modern technology uses MOS (metal-oxide-semiconductor) memory cells in integrated circuit (IC)
		chips for random-access memory. Although non-volatile RAM has also been developed, random access
		memory (RAM) is typically associated with volatile types of memory (such as dynamic
		random-access memory (DRAM) modules), where stored information is lost if power is removed.
		Other non-volatile memories exist that support random access for read operations but either do
		not support write operations or have other kinds of restrictions on them. These include the
		majority of ROM variants and the NOR-Flash class of flash memory.
	</p>
	<div class="divider" />
	<p>
		Static random-access memory (SRAM) and dynamic random-access memory are the two primary
		varieties of volatile random-access semiconductor memory (DRAM). IBM debuted the SP95 SRAM chip
		for their System/360 Model 95 computer in 1965, and Toshiba employed DRAM memory cells for its
		Toscal BC-1411 electronic calculator. Both of these devices were based on bipolar transistors.
		The development of commercial MOS memory, based on MOS transistors, in the late 1960s served as
		the foundation for all subsequent commercial semiconductor memory. The Intel 1103, the first
		DRAM IC chip for use in consumer electronics, was released in October 1970. The Samsung
		KM48SL2000 chip introduced synchronous dynamic random-access memory (SDRAM) a year later in
		1992.
	</p>
</div>


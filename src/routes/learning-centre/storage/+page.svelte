	<div class="text-4xl">Storage</div>
</div>

<div class="mx-20">
	<p>
		Digital data is a term that refers to data that is stored on a digital, machine-readable medium.
		One of a general-purpose computer's fundamental tasks is data storage. Compared to paper
		documents, electronic documents take up far less storage space.
	</p>
	<div class="divider" />
	<p>
		A fixed disc, hard disc, or hard disc drive (HDD) is an electro-mechanical data storage device
		that uses magnetic storage with one or more stiff, quickly rotating platters coated with
		magnetic material to store and retrieve digital data. The platters are paired with magnetic
		heads that read and write data to the platter surfaces. These heads are typically positioned on
		a moving actuator arm. Data is accessible randomly, which allows for the storing and retrieval
		of individual data blocks in any order. HDDs are a kind of non-volatile storage that keeps its
		data even after the power is turned off. The typical shape of a modern HDD is a tiny,
		rectangular box.
	</p>
	<div class="divider" />
	<p>
		Modern HDDs typically come in two sizes: 2.5 inches for laptops and 3.5 inches for desktop PCs.
		Standard interface cables, such as PATA (Parallel ATA), SATA (Serial ATA), USB, or SAS (Serial
		Attached SCSI) cables, are used to connect HDDs to computers.
	</p>
	<div class="divider" />
	<p>
		In the hierarchy of computer storage, a solid-state drive (SSD) is a solid-state storage device
		that uses integrated circuit assemblies to store data persistently, generally using flash
		memory. Despite the fact that SSDs do not have the physical spinning discs and moving read-write
		heads found in hard disc drives (HDDs) and floppy discs, they are sometimes referred to as
		semiconductor storage devices, solid-state devices, or solid-state discs. SSDs have a lot of
		intrinsic parallelism for processing data.
	</p>
	<div class="divider" />
	<p>
		SSDs are often more shock-resistant than electromechanical drives, operate quietly, offer better
		input/output rates, and lower latency. SSDs store data on semiconductor cells. As of 2019, cells
		can hold 1 to 4 bits of information. Single-bit cells (also known as "Single Level Cells" or
		"SLC") are typically the most dependable, durable, fast, and expensive type of SSD storage
		device, followed by 2- and 3-bit cells (also known as "Multi-Level Cells" or "MLC" and
		"Triple-Level Cells" or "TLC"), and finally quad-bit cells ("QLC"), which are used for consumer
		devices that do not need such extreme properties and are the least expensive per gigabyte of the
	</p>
	<div class="divider" />
	<p>
		Traditional HDD interfaces and form factors can be used with SSDs, or newer interfaces and form
		factors that take advantage of particular benefits of the flash memory of SSDs. Such SSDs can be
		utilised as drop-in replacements for HDDs in PCs and other devices thanks to conventional
		connections (such SATA and SAS) and conventional HDD form factors. More recent form factors
		include mSATA, M.2, U.2, NF1/M.3/NGSFF, XFM Express (Crossover Flash Memory, form factor XT2),
		EDSFF (previously known as Ruler SSD), and M.2, U.2, among others. Performance over HDDs can be
		further improved by using faster interfaces, as NVM Express (NVMe) via PCI Express (PCIe).
	</p>

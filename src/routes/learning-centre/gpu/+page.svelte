	<div class="text-4xl">GPU</div>
</div>

<div class="mx-20">
	<p>
		An electronic circuit known as a graphics processing unit (GPU) is specialised and created to
		manipulate and change memory in order to speed up the production of images in a frame buffer
		that are intended to be output to a display device. GPUs are utilised in game consoles, personal
		computers, workstations, mobile devices, and embedded systems.
	</p>
	<div class="divider" />
	<p>
		Modern GPUs are effective at handling image processing and computer graphics. They are more
		effective than general-purpose CPUs for algorithms that handle huge blocks of data concurrently
		due to their parallel structure. A GPU can be built into the motherboard of a personal computer
		or be found on a video card. They are incorporated into some CPUs' CPU dies.
	</p>
	<div class="divider" />
	<p>
		The word "GPU" was first used in the 1970s to refer to a programmable processing unit that
		operated independently from the CPU and was in charge of generating and manipulating visuals.
		Later, in 1994, Sony referred to the Toshiba-designed Sony GPU for the PlayStation console by
		the acronym (now standing for graphics processing unit). Nvidia made the phrase well-known by
		calling the GeForce 256 "the world's first GPU" in 1999. A "single-chip processor with
		integrated transform, lighting, triangle setup/clipping, and rendering engines" was how it was
		described in. When the Radeon 9700 was introduced by rival ATI Technologies in 2002, the name
		"visual processing unit" or VPU was first used.
	</p>
	<div class="divider" />
	<p>
		GPUs have been manufactured by numerous businesses under various brand names. With 49.4%, 27.8%,
		and 20.6% market shares, respectively, Intel, Nvidia, and AMD/ATI were the market share leaders
		in 2009. However, those figures count Intel's built-in graphics processors as GPUs. In 2018,
		Nvidia and AMD accounted for nearly all of the market, excluding those. 66% and 33% of the
		market, respectively, are theirs. In addition, GPUs are created by Matrox. In addition, most
		modern smartphones employ Imagination Technologies' PowerVR, Qualcomm's Adreno, and ARM's Mali
		GPUs.
	</p>
